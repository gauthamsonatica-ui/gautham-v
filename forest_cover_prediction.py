import pandas as pd
import pickle
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import LabelEncoder
from imblearn.over_sampling import SMOTE
from sklearn.metrics import accuracy_score, classification_report

# 1. Load Data
df_forest = pd.read_csv("https://drive.google.com/uc?export=download&id=1UzSSMGF9iqSb8YnSRXMt02v3uf9ZUOf6")

# 2. Encode the Target Variable
le = LabelEncoder()
df_forest['Cover_Type_Encoded'] = le.fit_transform(df_forest['Cover_Type'])

# 3. Separate Features and Target
X = df_forest.drop(['Cover_Type', 'Cover_Type_Encoded'], axis=1)
y = df_forest['Cover_Type_Encoded']

# 4. Handle Class Imbalance using SMOTE
smote = SMOTE(random_state=42)
X_resampled, y_resampled = smote.fit_resample(X, y)

# 5. Train-Test Split
X_train, X_test, y_train, y_test = train_test_split(X_resampled, y_resampled, test_size=0.2, random_state=42)

# 6. Train the Model (Random Forest)
print("Training the Random Forest model. This may take a minute...")
rf_model = RandomForestClassifier(n_estimators=100, random_state=42, n_jobs=-1)
rf_model.fit(X_train, y_train)

# 7. Quick Evaluation
y_pred = rf_model.predict(X_test)
print(f"Model Accuracy: {accuracy_score(y_test, y_pred) * 100:.2f}%")

# 8. Save the Model and Encoder for Streamlit
with open('best_model.pkl', 'wb') as f:
    pickle.dump(rf_model, f)
with open('label_encoder.pkl', 'wb') as f:
    pickle.dump(le, f)

print("✅ best_model.pkl and label_encoder.pkl successfully saved!")

